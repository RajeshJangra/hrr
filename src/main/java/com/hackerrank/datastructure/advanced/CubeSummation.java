public class CubeSummation {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int testCases = in.nextInt();
        for (int i = 0; i < testCases; i++) {
            Set<Point> points = new HashSet<>(in.nextInt());
            int queries = in.nextInt();
            while (queries-- > 0) {
                switch (in.next()) {
                    case "UPDATE":
                        final int a = in.nextInt();
                        final int b = in.nextInt();
                        final int c = in.nextInt();
                        final long value = in.nextLong();
                        final Point point1 = new Point(a, b, c, value);
                        points.remove(point1);
                        points.add(point1);
                        break;
                    case "QUERY":
                        long sum = 0;
                        final int a1 = in.nextInt();
                        final int b1 = in.nextInt();
                        final int c1 = in.nextInt();

                        final int a2 = in.nextInt();
                        final int b2 = in.nextInt();
                        final int c2 = in.nextInt();
                        for (Point point : points) {
                            if (point.x >= a1 && point.x <= a2 && point.y >= b1 && point.y <= b2 && point.z >= c1 && point.z <= c2) {
                                sum += point.value;
                            }
                        }
                        System.out.println(sum);
                }
            }

        }
    }

    static class Point {
        int x;
        int y;
        int z;
        long value;

        public Point(int x, int y, int z, long value) {
            this.x = x;
            this.y = y;
            this.z = z;
            this.value = value;
        }

        @Override
        public boolean equals(final Object o) {
            if (this == o) {
                return true;
            }
            if (o == null || getClass() != o.getClass()) {
                return false;
            }
            final Point point = (Point) o;
            return x == point.x && y == point.y && z == point.z;
        }

        @Override
        public int hashCode() {
            return Objects.hash(x, y, z);
        }
    }
}

